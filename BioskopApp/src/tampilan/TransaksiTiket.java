    private void txtJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyReleased
    try {
    if (!txtJumlah.getText().isEmpty() && !txtHarga.getText().isEmpty()) {
        int harga = Integer.parseInt(txtHarga.getText());
        int jumlah = Integer.parseInt(txtJumlah.getText());
        int total = harga * jumlah;
        lblTotal.setText(String.valueOf(total)); // Otomatis nulis angka gede di label
    } else {
        lblTotal.setText("0");
    }
} catch (NumberFormatException e) {
    lblTotal.setText("0"); // Kalau user iseng ngetik huruf, balikin ke nol
}
    }//GEN-LAST:event_txtJumlahKeyReleased

    private void btnBayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBayarActionPerformed
    try {
    // 1. Cari ID Film berdasarkan judul yang dipilih
    String judul = cbFilm.getSelectedItem().toString();
    int idFilm = 0;
    Connection con = new database.Koneksi().getConnection();
    PreparedStatement pstId = con.prepareStatement("SELECT id_film FROM film WHERE judul_film = ?");
    pstId.setString(1, judul);
    ResultSet rsId = pstId.executeQuery();
    if (rsId.next()) {
        idFilm = rsId.getInt("id_film");
    }

    // 2. Simpan ke database tiket
    String sql = "INSERT INTO tiket (id_film, nomor_kursi, jumlah_tiket, metode_bayar, total_harga) VALUES (?, ?, ?, ?, ?)";
    PreparedStatement pst = con.prepareStatement(sql);
    pst.setInt(1, idFilm);
    pst.setString(2, txtKursi.getText());
    pst.setInt(3, Integer.parseInt(txtJumlah.getText()));
    pst.setString(4, cbBayar.getSelectedItem().toString());
    pst.setInt(5, Integer.parseInt(lblTotal.getText()));

    pst.executeUpdate();

    // 3. Notifikasi Berhasil
    JOptionPane.showMessageDialog(null, "Pembayaran Berhasil! Data tersimpan ke database. 🎟️");
 // Membuka form CetakTiket dan mengirim data transaksinya dengan URUTAN YANG BENAR
    // Membuka form CetakTiket dan mengirim data transaksinya dengan URUTAN YANG BENAR
    CetakTiket struk = new CetakTiket();
    struk.tampilkanStruk(
        cbFilm.getSelectedItem().toString(),   // 1. Judul Film
        cbJadwal.getSelectedItem().toString(), // 2. Jadwal (Waktu)
        txtKursi.getText(),                    // 3. Nomor Kursi
        lblTotal.getText(),                    // 4. Total Harga
        cbBayar.getSelectedItem().toString()   // 5. Metode Bayar <--- PASTIKAN PAKAI INI
    );
    struk.setVisible(true);

    // 4. Bersihkan form buat transaksi selanjutnya
    cbFilm.setSelectedIndex(0);
    txtHarga.setText("");
    txtJumlah.setText("");
    txtKursi.setText("");
    lblTotal.setText("0");

} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Gagal Bayar: " + e.getMessage());
}
    }//GEN-LAST:event_btnBayarActionPerformed

    private void cbFilmItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbFilmItemStateChanged
    if (cbFilm.getSelectedIndex() > 0) {
        try {
            // Ambil id_film berdasarkan judul
            String judul = cbFilm.getSelectedItem().toString();
            Connection con = new database.Koneksi().getConnection();
            
            // Query ambil harga dan jadwal
            String sqlJadwal = "SELECT id_jadwal, hari, jam FROM jadwal WHERE id_film = (SELECT id_film FROM film WHERE judul_film = ?)";
            PreparedStatement pst = con.prepareStatement(sqlJadwal);
            pst.setString(1, judul);
            ResultSet rs = pst.executeQuery();
            
            cbJadwal.removeAllItems();
            cbJadwal.addItem("- Pilih Jadwal -");
            while (rs.next()) {
                // Format: Hari - Jam
                cbJadwal.addItem(rs.getString("hari") + " - " + rs.getString("jam"));
            }
            
            // (Tambahkan kodingan ambil harga yang sudah kamu punya sebelumnya di sini)
            
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_cbFilmItemStateChanged

