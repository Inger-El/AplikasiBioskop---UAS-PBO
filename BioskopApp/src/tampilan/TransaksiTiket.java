    private void loadFilm() {
        cbFilm.removeAllItems();
        cbFilm.addItem("-- Pilih Film --");
        try {
            Connection con = new database.Koneksi().getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT judul_film FROM film");
            while(rs.next()) {
                cbFilm.addItem(rs.getString("judul_film"));
            }
        } catch (Exception e) {
            System.out.println("Error load film: " + e.getMessage());
        }
    }
    private void cbFilmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbFilmActionPerformed
    if (cbFilm.getSelectedIndex() > 0) {
    try {
        String judul = cbFilm.getSelectedItem().toString();
        Connection con = new database.Koneksi().getConnection();
        PreparedStatement pst = con.prepareStatement("SELECT harga FROM film WHERE judul_film = ?");
        pst.setString(1, judul);
        ResultSet rs = pst.executeQuery();
        if (rs.next()) {
            txtHarga.setText(rs.getString("harga"));
        }
    } catch (Exception e) {
        System.out.println("Error tampil harga: " + e.getMessage());
    }
} else {
    txtHarga.setText("");
}
    }//GEN-LAST:event_cbFilmActionPerformed

