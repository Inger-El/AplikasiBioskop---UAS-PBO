    public void tampilData() {
    // 1. Membuat kerangka kolom tabel
    DefaultTableModel model = new DefaultTableModel();
    model.addColumn("ID Film");
    model.addColumn("Judul Film");
    model.addColumn("Genre");

    try {
        // 2. Memanggil koneksi yang sudah dibuat
        Koneksi k = new Koneksi();
        Connection con = k.getConnection();

        // 3. Mengambil data dari database
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM film");

        // 4. Memasukkan data ke dalam baris tabel
        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getString("id_film"),
                rs.getString("judul_film"),
                rs.getString("genre")
            });
        }
        // 5. Menempelkan data ke tabel UI
        tabelFilm.setModel(model);

    } catch (Exception e) {
        System.out.println("Gagal tampil data: " + e.getMessage());
    }
}
    
