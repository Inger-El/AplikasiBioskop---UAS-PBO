    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
   try {
            int baris = tabelFilm.getSelectedRow();
            if (baris == -1) {
                javax.swing.JOptionPane.showMessageDialog(null, "Pilih dulu data di tabel yang mau dihapus!");
                return;
            }

            // Jaring pengaman untuk ID
            Object valId = tabelFilm.getValueAt(baris, 0);
            String id = (valId != null) ? valId.toString() : "";

            database.Koneksi k = new database.Koneksi();
            java.sql.Connection con = k.getConnection();
            java.sql.PreparedStatement pst = con.prepareStatement("DELETE FROM film WHERE id_film = ?");
            pst.setString(1, id);
            pst.executeUpdate();

            javax.swing.JOptionPane.showMessageDialog(null, "Yess! Data berhasil dihapus 🗑️");
            tampilData();

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Gagal hapus: " + e.getMessage());
        }
    
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
    try {
            int baris = tabelFilm.getSelectedRow();
            if (baris == -1) {
                javax.swing.JOptionPane.showMessageDialog(null, "Pilih data yang mau diubah dulu dari tabel!");
                return;
            }

            // Jaring pengaman: Cek null sebelum diubah ke String
            Object valId = tabelFilm.getValueAt(baris, 0);
            String id = (valId != null) ? valId.toString() : "";

            Object valJudul = tabelFilm.getValueAt(baris, 1);
            String judul = (valJudul != null) ? valJudul.toString() : "";

            Object valGenre = tabelFilm.getValueAt(baris, 2);
            String genre = (valGenre != null) ? valGenre.toString() : "";

            // Membuka form UbahFilm
            UbahFilm formUbah = new UbahFilm();
            formUbah.isiData(id, judul, genre);
            formUbah.setVisible(true);
            this.dispose(); 

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(null, "Gagal membuka form ubah: " + e.getMessage());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

