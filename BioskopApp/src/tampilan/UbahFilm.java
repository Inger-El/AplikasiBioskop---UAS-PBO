    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
    try {
    // 1. Panggil koneksi database
    java.sql.Connection con = new database.Koneksi().getConnection();
    
    // 2. Kodingan update data
    String sql = "UPDATE film SET judul_film = ?, genre = ?, harga = ? WHERE id_film = ?";
    java.sql.PreparedStatement pst = con.prepareStatement(sql);
    
    pst.setString(1, txtJudul.getText());
    pst.setString(2, txtGenre.getText());
    pst.setString(3, txtHarga.getText());
    pst.setString(4, txtIdFilm.getText());
    
    pst.executeUpdate();
    
    // 3. Kasih notifikasi kalau berhasil
    javax.swing.JOptionPane.showMessageDialog(null, "Mantap! Data film dan harga berhasil diubah.");
    
} catch (Exception e) {
    // 4. Jaring pengaman kalau error
    javax.swing.JOptionPane.showMessageDialog(null, "Gagal mengubah data: " + e.getMessage());
}
    }//GEN-LAST:event_btnUbahActionPerformed

