    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {
    database.Koneksi k = new database.Koneksi();
    java.sql.Connection con = k.getConnection();
    
    // Query UPDATE, perhatikan urutan tanda tanya (?)
    String sql = "UPDATE film SET judul_film = ?, genre = ? WHERE id_film = ?";
    java.sql.PreparedStatement pst = con.prepareStatement(sql);
    
    pst.setString(1, txtJudul.getText());
    pst.setString(2, txtGenre.getText());
    pst.setString(3, txtIdFilm.getText()); // ID ada di urutan ke-3
    
    pst.executeUpdate();
    javax.swing.JOptionPane.showMessageDialog(null, "Mantap! Data berhasil diupdate 🔄");
    
    // Balik ke layar utama
    new TampilFilm().setVisible(true);
    this.dispose();
    
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(null, "Gagal update: " + e.getMessage());
}
    }//GEN-LAST:event_btnSimpanActionPerformed

